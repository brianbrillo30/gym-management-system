    public void tableSize () {
        
    jTable_members.setAutoResizeMode( JTable.AUTO_RESIZE_OFF );
    
        for (int column = 0; column < jTable_members.getColumnCount(); column++) {
            TableColumn tableColumn = jTable_members.getColumnModel().getColumn(column);
//            int preferredWidth = tableColumn.getMinWidth();
            int preferredWidth = 100;
            int maxWidth = tableColumn.getMaxWidth();

            for (int row = 0; row < jTable_members.getRowCount(); row++)
            {
                TableCellRenderer cellRenderer = jTable_members.getCellRenderer(row, column);
                Component c = jTable_members.prepareRenderer(cellRenderer, row, column);
                int width = c.getPreferredSize().width + jTable_members.getIntercellSpacing().width;
                preferredWidth = Math.max(preferredWidth, width);

                //  We've exceeded the maximum width, no need to check other rows

                if (preferredWidth >= maxWidth)
                {
                    preferredWidth = maxWidth;
                    break;
                }
        }

        tableColumn.setPreferredWidth( preferredWidth );

        }      
    }
    

    
