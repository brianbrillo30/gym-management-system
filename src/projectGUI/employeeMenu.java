    private void btn_browseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_browseActionPerformed
    
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        f = chooser.getSelectedFile();
        path = f.getAbsolutePath();
        ImageIcon micon=new ImageIcon(path);
        
        try {
            File imageProfile=new File(path);
            FileInputStream fis=new FileInputStream(imageProfile);
            ByteArrayOutputStream baos=new ByteArrayOutputStream();
            byte[] buf=new byte[1024];
            for(int readnum; (readnum=fis.read(buf)) !=-1;)
            {            
                baos.write(buf,0,readnum);                
            }
            pimage=baos.toByteArray();
            Image image = micon.getImage().getScaledInstance(lbl_image.getWidth(), lbl_image.getHeight(), Image.SCALE_SMOOTH);
            lbl_image.setIcon(new ImageIcon(image)); 
        } catch (Exception e) {
        }               
        

    }//GEN-LAST:event_btn_browseActionPerformed

