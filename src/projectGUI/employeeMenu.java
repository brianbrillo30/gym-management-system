    public void tableSize () {
        
    jTable_employee.setAutoResizeMode( JTable.AUTO_RESIZE_OFF );
    
        for (int column = 0; column < jTable_employee.getColumnCount(); column++) {
            TableColumn tableColumn = jTable_employee.getColumnModel().getColumn(column);
//            int preferredWidth = tableColumn.getMinWidth();
            int preferredWidth = 100;
            int maxWidth = tableColumn.getMaxWidth();

            for (int row = 0; row < jTable_employee.getRowCount(); row++)
            {
                TableCellRenderer cellRenderer = jTable_employee.getCellRenderer(row, column);
                Component c = jTable_employee.prepareRenderer(cellRenderer, row, column);
                int width = c.getPreferredSize().width + jTable_employee.getIntercellSpacing().width;
                preferredWidth = Math.max(preferredWidth, width);

                //  We've exceeded the maximum width, no need to check other rows

                if (preferredWidth >= maxWidth)
                {
                    preferredWidth = maxWidth;
                    break;
                }
        }

        tableColumn.setPreferredWidth( preferredWidth );

        }      
    }
    
    private void Load() {
         EmployeeDAOimp employee_dao = new EmployeeDAOimp();
        List<EmployeeClass> list = employee_dao.list();
        DefaultTableModel DFT = (DefaultTableModel) jTable_employee.getModel();
        DFT.setRowCount(0);
        for(EmployeeClass empc: list)
        {
            int id = empc.getId();
            int empID = empc.getEmployeeID();
            String first_name = empc.getFirstName();
            String middle_name = empc.getMiddleName();
            String last_name = empc.getLastName();
            String contact = empc.getContactNo();
            
            String birthday = empc.getBirthday();
            String emp_email = empc.getEmail();
            String gender = empc.getGender();
            String address = empc.getAddress();
            
            String staff_role = empc.getStaffRole();
            String shift = empc.getShift();
            
            double rate = empc.getRatePerHour();
            int hours = empc.getHoursWorked();
            double salary = empc.getSalary();
            DFT.addRow(new Object[]{id, empID, first_name, middle_name, last_name, contact, birthday, address,
            gender, emp_email, staff_role, shift, rate, hours, salary});
        }    
       
    }
